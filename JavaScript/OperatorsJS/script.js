let b = 2;

let c = a+b;
let c1 = a-b;
let c2 = a*b;
let c3 = a/b;
let c4 = a%b;

console.log("a =", a , "b =", b );

console.log("a + b = ", c );
console.log("a - b = ", c1);
console.log("a * b = ", c2);
console.log("a / b = ", c3);
console.log("a % b = ", c4);

